"""
Train synthetic-baseline pickles for the 7 sklearn ML adapters in
services/medverse-ai/src/ml/.

PURPOSE
=======
The real training pipelines under models/<pipeline>/ require multi-GB
public datasets (PTB-XL 25 GB, ICBHI 600 MB, HAM10000 1 GB, ODIR-5K
3.5 GB), credentials (Kaggle, PhysioNet, HuggingFace), and weeks of ML
engineering to fill in the NotImplementedError stubs in each
src/components/*.py.

This script ships **demo-only baselines** so the LLM agent's
`tool_results` block has structured ML output during integration
testing. The pickles match the runtime adapter format exactly:
    { "preprocessor": Pipeline, "model": <classifier|regressor> }

⚠ THESE MODELS ARE NOT CLINICALLY VALID.
⚠ They are trained on synthetic data with weak rule-based labels and
  exist only to exercise the wiring. Replace with weights from a real
  trained pipeline before any clinical claim. Each output dir gets a
  MODEL_CARD.md documenting this.

USAGE
=====
    cd <repo root>
    python scripts/train_synthetic_baselines.py

Writes:
    services/medverse-ai/models/cardiology/ecg_arrhythmia/model.pkl
    services/medverse-ai/models/cardiology/cardiac_age/model.pkl
    services/medverse-ai/models/pulmonary/lung_sound/model.pkl
    services/medverse-ai/models/obstetrics/preterm_labour/model.pkl
    services/medverse-ai/models/dermatology/skin_disease/model.pkl
    services/medverse-ai/models/ocular/retinal_disease/model.pkl
    services/medverse-ai/models/ocular/retinal_age/model.pkl
    + a MODEL_CARD.md alongside each.
"""
