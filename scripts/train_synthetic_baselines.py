def _write_card(out_dir: Path, **kw):
    (out_dir / "MODEL_CARD.md").write_text(_MODEL_CARD.format(**kw), encoding="utf-8")


# ─── Generic save helper ─────────────────────────────────────────────


def _build_classifier_pipeline(numeric_cols: List[str], n_estimators: int = 60) -> Pipeline:
    """Standard tabular pipeline: median-impute + standard-scale + RF."""
    pre = ColumnTransformer([
        ("num", Pipeline([
            ("impute", SimpleImputer(strategy="median")),
            ("scale", StandardScaler()),
        ]), numeric_cols),
    ])
    model = RandomForestClassifier(
        n_estimators=n_estimators, max_depth=6, random_state=42, n_jobs=1,
    )
    return Pipeline([("preprocessor", pre), ("model", model)])


def _build_regressor_pipeline(numeric_cols: List[str]) -> Pipeline:
    pre = ColumnTransformer([
        ("num", Pipeline([
            ("impute", SimpleImputer(strategy="median")),
            ("scale", StandardScaler()),
        ]), numeric_cols),
    ])
    model = GradientBoostingRegressor(
        n_estimators=60, max_depth=3, random_state=42,
    )
    return Pipeline([("preprocessor", pre), ("model", model)])


def _save_pipeline(out_path: Path, pipe: Pipeline) -> int:
    """Split the fitted Pipeline into the {preprocessor, model} dict
    the runtime PickledTabularAdapter expects, save, return file size."""
    out_path.parent.mkdir(parents=True, exist_ok=True)
    payload = {
        "preprocessor": pipe.named_steps["preprocessor"],
        "model": pipe.named_steps["model"],
    }
    with open(out_path, "wb") as f:
        pickle.dump(payload, f, protocol=pickle.HIGHEST_PROTOCOL)
    return out_path.stat().st_size


# ─── Generators (one per adapter) ────────────────────────────────────


def _gen_ecg_arrhythmia(n: int = 800) -> Tuple[pd.DataFrame, np.ndarray]:
    age = RNG.integers(20, 90, size=n)
    sex = RNG.integers(0, 2, size=n)
    hr = RNG.normal(75, 18, size=n).clip(40, 180)
    hrv = RNG.normal(45, 25, size=n).clip(5, 150)
    has_norm = ((hr >= 60) & (hr <= 100) & (hrv >= 25)).astype(int)
    has_mi = (RNG.random(n) < 0.06).astype(int)
    has_sttc = (hrv < 15).astype(int)
    df = pd.DataFrame({
        "age": age, "sex": sex,
        "has_norm": has_norm, "has_mi": has_mi, "has_sttc": has_sttc,
    })
    # Labels weighted by features so the model learns SOMETHING non-trivial.
    labels = []
    for _, row in df.iterrows():
        if row["has_mi"]:
            labels.append("MI")
        elif row["has_sttc"]:
            labels.append("STTC")
        elif row["has_norm"]:
            labels.append("NORM" if RNG.random() < 0.7 else "CD")
        else:
            labels.append(RNG.choice(["NORM", "CD", "HYP"], p=[0.5, 0.3, 0.2]))
    return df, np.array(labels)


def _gen_cardiac_age(n: int = 600) -> Tuple[pd.DataFrame, np.ndarray]:
    sex = RNG.integers(0, 2, size=n)
    axis_left = RNG.integers(0, 2, size=n)
    axis_right = RNG.integers(0, 2, size=n)
    df = pd.DataFrame({
        "sex": sex,
        "heart_axis_left": axis_left,
        "heart_axis_right": axis_right,
    })
    # Synthetic biological age — male slight bump, axis abnormalities push older
    bio_age = (
        RNG.normal(50, 15, size=n)
        + 3 * sex
        + 6 * axis_left
        + 4 * axis_right
        + RNG.normal(0, 4, size=n)
    ).clip(20, 95)
    return df, bio_age


def _gen_lung_sound(n: int = 700) -> Tuple[pd.DataFrame, np.ndarray]:
    duration_s = RNG.uniform(2.0, 8.0, size=n)
    rms = RNG.uniform(0.005, 0.25, size=n)
    zcr = RNG.uniform(0.05, 0.55, size=n)
    p95 = RNG.uniform(0.1, 0.8, size=n)
    p05 = RNG.uniform(-0.8, -0.1, size=n)
    df = pd.DataFrame({
        "duration_s": duration_s, "rms": rms, "zcr": zcr,
        "p95": p95, "p05": p05,
    })
    labels = []
    classes = ["Healthy", "COPD", "Asthma", "Pneumonia",
               "Bronchiectasis", "Bronchiolitis", "URTI", "LRTI"]
    for _, row in df.iterrows():
        if row["rms"] < 0.05:
            labels.append(RNG.choice(["Healthy", "URTI"], p=[0.85, 0.15]))
        elif row["zcr"] > 0.4:
            labels.append(RNG.choice(["COPD", "Asthma", "Bronchitis" if False else "Bronchiectasis"], p=[0.5, 0.35, 0.15]))
        elif row["p95"] - row["p05"] > 1.0:
            labels.append(RNG.choice(["Pneumonia", "LRTI"], p=[0.6, 0.4]))
        else:
            labels.append(RNG.choice(classes, p=[0.3, 0.1, 0.1, 0.15, 0.1, 0.1, 0.1, 0.05]))
    return df, np.array(labels)


def _gen_preterm_labour(n: int = 600) -> Tuple[pd.DataFrame, np.ndarray]:
    cols = [
        "gestational_age_weeks", "rms_emg", "mean_emg",
        "median_freq_emg", "peak_freq_emg",
        "contractions_per_min", "contraction_amplitude_mean",
        "interval_mean_s", "interval_cv",
    ]
    data = {
        "gestational_age_weeks": RNG.integers(22, 40, size=n),
        "rms_emg": RNG.uniform(0.05, 0.6, size=n),
        "mean_emg": RNG.uniform(0.0, 0.3, size=n),
        "median_freq_emg": RNG.uniform(0.2, 1.5, size=n),
        "peak_freq_emg": RNG.uniform(0.3, 2.5, size=n),
        "contractions_per_min": RNG.uniform(0.0, 6.0, size=n),
        "contraction_amplitude_mean": RNG.uniform(0.0, 0.7, size=n),
        "interval_mean_s": RNG.uniform(20.0, 600.0, size=n),
        "interval_cv": RNG.uniform(0.1, 0.9, size=n),
    }
    df = pd.DataFrame(data, columns=cols)
    # Preterm risk loosely tied to early gestation + high EMG activity
    score = (
        (37 - df["gestational_age_weeks"]).clip(0, None) / 15.0
        + df["rms_emg"]
        + df["contractions_per_min"] / 6.0
    )
    labels = np.where(score > 1.4, "PretermRisk", "Term")
    return df, labels


def _gen_skin_disease(n: int = 500) -> Tuple[pd.DataFrame, np.ndarray]:
    df = pd.DataFrame({"age": RNG.integers(5, 90, size=n)})
    classes = ["akiec", "bcc", "bkl", "df", "mel", "nv", "vasc"]
    # Older skews toward malignant categories
    labels = []
    for age in df["age"]:
        if age > 65:
            labels.append(RNG.choice(classes, p=[0.18, 0.18, 0.15, 0.05, 0.12, 0.27, 0.05]))
        elif age > 40:
            labels.append(RNG.choice(classes, p=[0.06, 0.10, 0.15, 0.05, 0.10, 0.49, 0.05]))
        else:
            labels.append(RNG.choice(classes, p=[0.02, 0.02, 0.10, 0.07, 0.04, 0.70, 0.05]))
    return df, np.array(labels)


def _gen_retinal_disease(n: int = 600) -> Tuple[pd.DataFrame, np.ndarray]:
    df = pd.DataFrame({
        "age": RNG.integers(15, 90, size=n),
        "sex_male": RNG.integers(0, 2, size=n),
    })
    classes = ["Normal", "Diabetes", "Glaucoma", "Cataract",
               "AMD", "Hypertensive", "Myopia", "Other"]
    labels = []
    for age in df["age"]:
        if age > 70:
            labels.append(RNG.choice(classes, p=[0.18, 0.15, 0.12, 0.20, 0.18, 0.07, 0.05, 0.05]))
        elif age > 50:
            labels.append(RNG.choice(classes, p=[0.32, 0.18, 0.10, 0.10, 0.08, 0.10, 0.07, 0.05]))
        else:
            labels.append(RNG.choice(classes, p=[0.55, 0.10, 0.05, 0.02, 0.02, 0.08, 0.13, 0.05]))
    return df, np.array(labels)


def _gen_retinal_age(n: int = 500) -> Tuple[pd.DataFrame, np.ndarray]:
    df = pd.DataFrame({"sex_male": RNG.integers(0, 2, size=n)})
    # Synthetic bio age — modest sex effect
    bio_age = (RNG.normal(55, 16, size=n) + 2 * df["sex_male"]).clip(20, 95)
    return df, bio_age.values


def _gen_pulmonary_classifier(n: int = 600) -> Tuple[pd.DataFrame, np.ndarray]:
    """Same feature shape as _extract_features in pulmonary_classifier.py."""
    df = pd.DataFrame({
        "duration_s": RNG.uniform(2.0, 8.0, size=n),
        "rms": RNG.uniform(0.005, 0.4, size=n),
        "zcr": RNG.uniform(0.05, 0.6, size=n),
        "p95": RNG.uniform(0.1, 0.9, size=n),
        "p05": RNG.uniform(-0.9, -0.1, size=n),
    })
    classes = ["normal", "wheeze", "crackle", "stridor"]
    labels = []
    for _, row in df.iterrows():
        if row["rms"] < 0.05 and row["zcr"] < 0.2:
            labels.append("normal")
        elif row["zcr"] > 0.45:
            labels.append(RNG.choice(["wheeze", "stridor"], p=[0.7, 0.3]))
        elif row["p95"] - row["p05"] > 1.4:
            labels.append("crackle")
        else:
            labels.append(RNG.choice(classes, p=[0.45, 0.25, 0.20, 0.10]))
    return df, np.array(labels)


def _gen_ecgfounder(n: int = 700) -> Tuple[pd.DataFrame, np.ndarray]:
    """Same feature shape as _extract_features in ecgfounder_adapter.py."""
    df = pd.DataFrame({
        "duration_s": RNG.uniform(5.0, 30.0, size=n),
        "hr_estimate": RNG.normal(75, 22, size=n).clip(35, 200),
        "hrv_proxy": RNG.uniform(5, 200, size=n),
        "signal_rms": RNG.uniform(0.05, 0.8, size=n),
        "p95_p05_range": RNG.uniform(0.2, 3.0, size=n),
        "peak_interval_cv": RNG.uniform(0.0, 0.5, size=n),
    })
    labels = []
    for _, row in df.iterrows():
        if row["hr_estimate"] > 130 and row["peak_interval_cv"] > 0.15:
            labels.append("Atrial Fibrillation")
        elif row["hr_estimate"] < 50:
            labels.append("Bundle Branch Block")
        elif row["peak_interval_cv"] > 0.20:
            labels.append(RNG.choice(["Premature Atrial Contraction",
                                      "Premature Ventricular Contraction"], p=[0.55, 0.45]))
        elif row["p95_p05_range"] > 2.2:
            labels.append(RNG.choice(["ST Elevation", "ST Depression"], p=[0.55, 0.45]))
        elif 60 <= row["hr_estimate"] <= 100 and row["hrv_proxy"] > 25:
            labels.append("Sinus Rhythm" if RNG.random() < 0.85 else "Other")
        else:
            labels.append(RNG.choice(
                ["Sinus Rhythm", "Other", "Premature Atrial Contraction"],
                p=[0.6, 0.3, 0.1]))
    return df, np.array(labels)


# ─── Driver ──────────────────────────────────────────────────────────


def _train_one(
    label: str,
    pipeline_dir: str,            # e.g., "ecg_arrhythmia"
    out_subpath: str,             # e.g., "cardiology/ecg_arrhythmia/model.pkl"
    adapter_module: str,          # e.g., "ecg_arrhythmia_adapter"
    gen: Callable[[], Tuple[pd.DataFrame, np.ndarray]],
    is_classifier: bool,
    n_samples: int,
    labels_text: str,
):
    df, y = gen()
    if is_classifier:
        pipe = _build_classifier_pipeline(list(df.columns))
    else:
        pipe = _build_regressor_pipeline(list(df.columns))
    pipe.fit(df, y)
    out_path = MODELS_ROOT / out_subpath
    size_b = _save_pipeline(out_path, pipe)
    _write_card(
        out_path.parent,
        label=label,
        kind="classifier" if is_classifier else "regressor",
        n_samples=n_samples,
        size_kb=max(1, size_b // 1024),
        pipeline=pipeline_dir,
        adapter_module=adapter_module,
        features_md="\n".join(f"- `{c}`" for c in df.columns),
        labels_md=labels_text,
    )
    print(f"  ✓ {out_subpath:55s} {size_b/1024:7.1f} KB")


